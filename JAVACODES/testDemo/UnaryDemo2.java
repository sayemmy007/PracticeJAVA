package testDemo;

public class UnaryDemo2 {

	public static void main(String[] args) {
		
		int i = 3;
		
		i++;   // print 4
		System.out.println(i);
		
		++i;  // print 5
		System.out.println(i);
		
		System.out.println(++i); // print 6
		
		System.out.println(i++); // print 6 because it assign 1st and then icrement
		
		System.out.println(i); // print 7
		
		
		
		
		
	}

}
