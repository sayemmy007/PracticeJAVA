package testDemo;

public class ImplicitDataTypeConversion {

	public static void main(String[] args) {
	
		int a = 100;
		System.out.println("Representation of int="+a);
		
		long b = a;
		System.out.println("Representation of long="+b);
		
		double c = a;
		System.out.println("Representation of long="+c);
	}

}
