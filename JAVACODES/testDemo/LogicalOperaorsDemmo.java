package testDemo;

public class LogicalOperaorsDemmo {

	public static void main(String[] args) {
		
		int value1 = 1;
		int value2 = 2;
		
		if((value1 == 1) && (value2 == 2)) {
			System.out.println("value 1 is 1 AND value 2 is 2");
		}
		
		if((value1 == 2) || (value2 == 4)) {
			System.out.println("value 1 is 2 AND value 2 is 4");
		}
	}

}
